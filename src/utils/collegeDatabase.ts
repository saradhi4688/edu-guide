// Function to filter colleges based on user preferences
export const filterColleges = (
  colleges: College[],
  filters: {
    courses?: string[];
    location?: {lat: number, lng: number, radius: number};
    type?: string[];
    feeRange?: {min: number, max: number};
    rating?: number;
  }
): College[] => {
  return colleges.filter(college => {
    // Course filter
    if (filters.courses && filters.courses.length > 0) {
      const hasMatchingCourse = filters.courses.some(course => 
        college.courses.some(collegeCourse => 
          collegeCourse.toLowerCase().includes(course.toLowerCase()) ||
          course.toLowerCase().includes(collegeCourse.toLowerCase().split(' ')[0])
        )
      );
      if (!hasMatchingCourse) return false;
    }

    // Location filter
    if (filters.location) {
      const distance = calculateDistance(
        filters.location.lat,
        filters.location.lng,
        college.latitude,
        college.longitude
      );
      if (distance > filters.location.radius) return false;
    }

    // Type filter
    if (filters.type && filters.type.length > 0) {
      if (!filters.type.includes(college.type)) return false;
    }

    // Fee range filter (simplified - would need proper parsing in real app)
    if (filters.feeRange) {
      const feeValue = parseFloat(college.fees.replace(/[₹,LPA\s]/g, ''));
      if (feeValue < filters.feeRange.min || feeValue > filters.feeRange.max) return false;
    }

    // Rating filter
    if (filters.rating && college.rating < filters.rating) return false;

    return true;
  });
};

// Calculate distance between two points (already defined in dataStore, but repeated for modularity)
const calculateDistance = (lat1: number, lon1: number, lat2: number, lon2: number): number => {
  const R = 6371; // Radius of the Earth in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  const distance = R * c; // Distance in km
  return Math.round(distance * 10) / 10; // Round to 1 decimal place
};

